	MX_TIM1_Init();
	/* USER CODE BEGIN 2 */
	HAL_Delay(2000);
	log_message(&huart2, "Initializing DHT11 Sensor...\r\n");

	dht11_init(&dht11_sensor, GPIOA, GPIO_PIN_1, &htim1);
	dht11_response initial_response;
	uint8_t retries = 0;
	do {
		initial_response = dht11_read(&dht11_sensor);
		if (initial_response.status != DHT11_OK) {
			retries++;
			log_message(&huart2, "DHT11 Initialization retry %d/%d: %s\r\n", retries,
					DHT11_MAX_INIT_RETRIES,
					dht11_status_to_string(initial_response.status));
			HAL_Delay(2000);
		}
	} while (initial_response.status != DHT11_OK
			&& retries < DHT11_MAX_INIT_RETRIES);

	if (initial_response.status == DHT11_OK) {
		log_message(&huart2, "DHT11 Sensor Initialized.\r\n\n");
	} else {
		log_message(&huart2, "FATAL: DHT11 Initialization Failed.\r\n\n");
		return 1;
	}

	HAL_Delay(2000);
	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1)

	{
		dht11_response response = dht11_read(&dht11_sensor);
		if (response.status != DHT11_OK) {
			log_message(&huart2, "Error: %s\r\n\n",
					dht11_status_to_string(response.status));
		} else {
			log_message(&huart2,
					"Temperature: %.2f C\r\nHumidity: %.2f RH\r\n\n",
					response.temperature, response.humidity);
		}
		HAL_Delay(2000);
		/* USER CODE END WHILE */

		/* USER CODE BEGIN 3 */
	}
